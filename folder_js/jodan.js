let jodanList = JSON.parse(localStorage.getItem("jodanList"));
let jodanProductcontainer = document.getElementById("jodan-section");

let jodanProduct = jodanList.filter(function (e, i) {
  return e.category === 2;
});

// check đăng nhập
let iconLogin = document.getElementById("icon-login");
iconLogin.addEventListener("click", function () {
  let checkIconLogin = JSON.parse(localStorage.getItem("checkLogin"));
  if (checkIconLogin) {
    window.location.href = "/folder_pages/myaccount.html";
  } else {
    window.location.href = "/folder_pages/dangnhap.html";
  }
});
let totalItem = jodanList.length;
let itempage = 8;
let totalPage = Math.ceil(totalItem / itempage);
let currentPage = 0;
let start = 0;
let end = 0;
let pagination = document.getElementsByClassName("pagination")[0];
for (let i = 1; i < totalPage + 1; i++) {
  console.log(i);
  pagination.innerHTML += `
  <div class="chuyentrang">
    <a href="#" onclick="jodantrang(${i})">${i}</a>
  </div>
  `;
}
// tong so trang 5 trang
function jodantrang(page) {
  currentPage = page;
  start = (currentPage - 1) * itempage;
  end = currentPage * itempage;
  renderJodan();
}
function renderJodan() {
  jodanProductcontainer.innerHTML = "";
  for (let i = 0; i < jodanList.length; i++) {
    if (i >= start && i < end) {
      jodanProductcontainer.innerHTML += `
        <div class="product" id="product-container-1">
            <div class="photo">
                <img src="${jodanProduct[i].img}" style="width:100%" alt="photo" />
            </div>

            <h4 class="h4-product">
                ${jodanProduct[i].name}
            </h4>
            <p class="value">₫ ${jodanProduct[i].price}</p>
            <div class="tong">
                <div class="icons icon-heart">
                <i class="fa-regular fa-heart"></i>
                </div>
                <button class="timhieuthem"><a href="#" style="text-decoration: none">Tìm hiểu thêm</a></button>
                <div class="icons icon-heart" id="${jodanProduct[i].id}" onclick ="addToCart(${jodanProduct[i].id})">
                  <ion-icon name="cart-outline"></ion-icon>
                </div>
            </div>
        </div>`;
    }
  }
}
jodantrang(1);
