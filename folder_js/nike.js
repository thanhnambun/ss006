let nikeList = JSON.parse(localStorage.getItem("nikeList"));
let nikeProductcontainer = document.getElementById("nike-section");

let nikeProduct = nikeList.filter(function (e, i) {
  return e.category === 3;
});

// check đăng nhập
let iconLogin = document.getElementById("icon-login");
iconLogin.addEventListener("click", function () {
  let checkIconLogin = JSON.parse(localStorage.getItem("checkLogin"));
  if (checkIconLogin) {
    window.location.href = "/folder_pages/myaccount.html";
  } else {
    window.location.href = "/folder_pages/dangnhap.html";
  }
});

// so san pham tren 1 trang là 6 sản phẩm
let totalItem = nikeList.length;
let itempage = 8;
let totalPage = Math.ceil(totalItem / itempage);
let currentPage = 0;
let start = 0;
let end = 0;
let pagination = document.getElementsByClassName("pagination")[0];
for (let i = 1; i < totalPage + 1; i++) {
  console.log(i);
  pagination.innerHTML += `
  <div class="chuyentrang">
    <a href="#" onclick="niketrang(${i})">${i}</a>
  </div>
  `;
}
// tong so trang 5 trang
function niketrang(page) {
  currentPage = page;
  start = (currentPage - 1) * itempage;
  end = currentPage * itempage;
  renderNike();
}
function renderNike() {
  nikeProductcontainer.innerHTML = "";
  for (let i = 0; i < nikeList.length; i++) {
    if (i >= start && i < end) {
      nikeProductcontainer.innerHTML += `
        <div class="product" id="product-container-1">
            <div class="photo">
                <img src="${nikeProduct[i].img}" style="width:100%" alt="photo" />
            </div>

            <h4 class="h4-product">
                ${nikeProduct[i].name}
            </h4>
            <p class="value">₫ ${nikeProduct[i].price}</p>
            <div class="tong">
                <div class="icons icon-heart">
                <i class="fa-regular fa-heart"></i>
                </div>
                <button class="timhieuthem"><a href="#" style="text-decoration: none">Tìm hiểu thêm</a></button>
                <div class="icons icon-heart" id="${nikeProduct[i].id}" onclick ="addToCart(${nikeProduct[i].id})">
                  <ion-icon name="cart-outline"></ion-icon>
                </div>
            </div>
        </div>`;
    }
  }
}
niketrang(1);
